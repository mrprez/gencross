package com.mrprez.gencross.formula;

public class MalformedFormulaException extends Exception {
	private static final long serialVersionUID = 1L;

	public MalformedFormulaException() {
		super();
	}

	public MalformedFormulaException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}

	public MalformedFormulaException(String arg0) {
		super(arg0);
	}

	public MalformedFormulaException(Throwable arg0) {
		super(arg0);
	}

	
	
}
